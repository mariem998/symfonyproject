    // Find/search articles by title/content
    public function findProductByName(string $query)
    {
        $qb = $this->createQueryBuilder('p');
        $qb
            ->where(
                $qb->expr()->andX(
                    $qb->expr()->orX(
                        $qb->expr()->like('p.name', ':query'),

                    ),

                )
            )
            ->setParameter('query', '%' . $query . '%') ;
        return $qb
            ->getQuery()
            ->getResult();
    }

